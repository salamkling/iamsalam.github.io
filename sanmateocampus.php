          <div class="row p-2" style="font-family: 'Roboto', sans-serif; font-weight: 500;">
         <h4>HISTORICAL BACKGROUND</h4>
          </div>
          <div class="row mb-2 p-3" style="font-weight: 300; font-size: 20px; color: black; text-align: justify-all;">
            <b>
            <p style="text-align: justify;">The Isabela State University – San Mateo campus proudly stands at its new location after 20 years of stay at the Barangay Hall. San Mateo campus stands on a ground that was once an agricultural land owned by generous Col. Andres D. Damian. Although it is not big campus as its mother campus, it houses a family of teachers and students, who have brilliant minds and creative hands in achieving excellence in Instruction, production, research and extension.</p>

            <p style="text-align: justify;">ISU San Mateo campus has gone long in its effort to work for the betterment of the students. At 34thyear ISU San Mateo remains steadfast in its mission of giving quality education.</p>

            <p style="text-align: justify;">It is a fact that ISU – San Mateo has never been spared of problems in its commitments to uphold quality education and instruction including services. However, it has surpassed the test of time with its capacity for survival that even in the midst of constant change of societal demands and challenges; it has proven its competence as proven by the result of the recent celebration of its silver founding anniversary.</p>
            <p>ISU San Mateo remains open to more complex challenges in sustaining high standard education.</p>

            <p style="text-align: justify;">The San Mateo Vocational and Industrial School which started in 1976 had a two-year Technical College Department which consequently became the nucleus of the Isabela State University San Mateo Campus in pursuant of Section 15 of Presidential Decree 1434.</p>

            <p style="text-align: justify;">In P.D. 1434, President Ferdinand E. Marcos established the Isabela State University (ISU), the first in Region 02, on June 10, 1978 by merging the Cagayan Valley Institute of Technology (CVIT) at Cabagan, the Isabela State College of Agriculture (ISCA) at Echague and the absorption of college courses from the Isabela School of Arts and Trades (ISAT) in Ilagan, Roxas Memorial Agriculture and Industrial School (RMAIS) in Roxas and San Mateo Vocational and Industrial School (SMVIS) in San Mateo and Jones Rural School, Jones, Isabela.</p>

            <p style="text-align: justify;">Upon the creation of ISU-San Mateo, Mr. Francisco Juan, the SMVIS School Administrator, was appointed as the Officer-in-Charge followed by Prof. Sotero G. Martin as the first appointed Campus Head (1979-1982); Prof. Avelina L. Domingo, Chairman (1982-1987); Dr. Jose C. Domingo, Chairman (June 1987-1996); Dr. Avelina L. Domingo (1996-May 31, 2002); Prof. Quirino G. Lucas, Campus Executive Director (June 1, 2002-August 2005); Dr. Dr. Reynaldo L. Raymundo, Campus Executive Director (August 2005 – December 3, 2008); Prof. Dalmacio G. Leaño – December 4, 2008 – June 30, 2008; and Dr. Leon M. Gonzales, Campus Administrator.</p>

            <p style="text-align: justify;">In 1980 through the generosity of Barangay Capt. Hardy B. Lintao, the University occupied temporarily the Mother Craft Center and the Pre-Fab Building of Brgy. San Andres as its Library and classrooms respectively until such time that the University was transferred to a portion of lot number 2085- F TCT 141877 with an area of 1.2 hectares located at National Highway San Andres, San Mateo,Isabela in 1999. The lot was donated by the late Col. Andres D. Damian through the initiative of the administration of Dr. Avelina L. Domingo.</p>

            <p style="text-align:: justify;">At present, the University offers academic courses namely: Bachelor of Science in Industrial Education (Degree and Ladderized) with major fields of specialization in Garments Technology, Food Technology, Electrical Technology, Applied Electronics Technology, Drafting Technology, Foods Technology, Bachelor of Science in Information Technology (Degree and Ladderized), Bachelor of Science in Agriculture (First two-years), Bachelor of Arts major in Political Science, Bachelor in Secondary Education, two-Year courses like Computer Secretarial and Computer Programming.</p>

            <p style="text-align: justify;">The University’s management is peopled by effective and efficient faculty and staff. Work forces include 1 Professor V, 2 Associate Professor V, 1 Associate Professor II, 2 Assistant Professor IV, 1 Assistant Professor III, 1 Assistant Professor I, 3 Instructor I, 7 Contractual Instructor I, 1 Part-Time Instructor, 3 Support Staff members, 1 utility worker, and 3 security guards.</p>

            <p style="text-align: justify;">From its humble beginnings, the growth of ISU-San Mateo Campus has been phenomenal. It has provided quality education to students not only from San Mateo but also from nearby towns like Ramon, Cabatuan, Aurora, San Manuel, Quezon, and other towns in Mountain province and Kalinga. It is hoped that it will continuously bring to life the dreams of our people to be educated and be educated well.</p>
            </b>
